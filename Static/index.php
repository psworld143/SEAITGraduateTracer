                        <h3>Our History</h3>
                        <p>South East Asian Institute of Technology, Inc., affectionately known as SEAIT, began its
                            journey in February 2006. Unlike a typical foundation story, ours unfolded like a dramatic
                            play, with all the elements of a grand performance. The curtains opened slowly, revealing
                            our humble beginnings.</p>
                        <p>Initially occupying a rented space at a Shell Gasoline station in Tupi, SEAIT catered to a
                            small population of vocational students. The founders, Mr. Reynaldo S. Tamayo and Mrs.
                            Rochelle P. Tamayo, faced numerous challenges, particularly regarding the learning
                            environment. Despite these obstacles, they remained steadfast in their commitment to
                            providing quality education.</p>
                        <p>SEAIT began by offering technical and vocational courses, such as Computer Programming NC IV
                            and Computer Hardware Servicing NC II. A pivotal moment came when the Technical Education
                            Skills Development Authority (TESDA) granted permission to offer Hotel and Restaurant
                            Management. Soon after, the Commission on Higher Education (CHED) approved our first
                            four-year program, the Bachelor of Science in Information Technology (BSIT), attracting a
                            broader audience.</p>
                        <p>Our momentum continued as CHED authorized a laddered Bachelor of Science in Hotel and
                            Restaurant Management and a Bachelor of Science in Business Administration, majoring in
                            Marketing Management. Within two years, we expanded to include Bachelor of Elementary
                            Education and Bachelor of Secondary Education, majoring in English and Mathematics.
                            Subsequently, we successfully introduced programs in Criminology, Agriculture, and Civil
                            Engineering.</p>
                        <p>In June 2016, we received government recognition from the Department of Education to operate
                            K-12 programs, including kindergarten, junior high, and senior high school, which saw steady
                            growth in enrollment.</p>
                        <p>This year, we proudly broaden our offerings with new degree programs:</p>
                        <ul>
                            <li>Bachelor of Science in Tourism Management (BSTM)</li>
                            <li>Bachelor of Early Childhood Education (BECE)</li>
                            <li>Bachelor of Science in Accounting Information Systems (BSAIS)</li>
                            <li>Bachelor of Science in Public Administration (BSPA)</li>
                            <li>Bachelor of Science in Fisheries (BSF)</li>
                        </ul>
                        <p>From the dedicated efforts of the Tamayo family, SEAIT has evolved into a vibrant community
                            of administrators, faculty, students, and local partners, all united in the pursuit of
                            education and meaningful contributions to society. Our story continues to unfold, driven by
                            our commitment to growth and excellence.</p>
